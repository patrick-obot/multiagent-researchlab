async def insert_finding(
    db: aiosqlite.Connection,
    *,
    id: str,
    title: str,
    summary: str | None = None,
    source_type: str,
    source_url: str | None = None,
    topic_tags: str | None = None,
    raw_content: str | None = None,
    scout_pi: str | None = None,
    status: str = "scouted",
) -> None:
    await db.execute(
        """INSERT INTO findings
           (id, title, summary, source_type, source_url, topic_tags,
            raw_content, scout_pi, status, discovered_at)
           VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)""",
        (id, title, summary, source_type, source_url, topic_tags,
         raw_content, scout_pi, status, _now()),
    )
    await db.commit()


async def get_finding(db: aiosqlite.Connection, finding_id: str) -> Row | None:
    async with db.execute(
        "SELECT * FROM findings WHERE id = ?", (finding_id,)
    ) as cur:
        return await cur.fetchone()


async def list_findings(
    db: aiosqlite.Connection,
    *,
    status: str | None = None,
    topic: str | None = None,
    limit: int = 50,
    offset: int = 0,
) -> list[Row]:
    clauses: list[str] = []
    params: list[Any] = []
    if status:
        clauses.append("status = ?")
        params.append(status)
    if topic:
        clauses.append("topic_tags LIKE ?")
        params.append(f"%{topic}%")
    where = ("WHERE " + " AND ".join(clauses)) if clauses else ""
    params += [limit, offset]
    async with db.execute(
        f"SELECT * FROM findings {where} ORDER BY discovered_at DESC LIMIT ? OFFSET ?",
        params,
    ) as cur:
        return await cur.fetchall()


async def update_finding_status(
    db: aiosqlite.Connection, finding_id: str, status: str
) -> None:
    await db.execute(
        "UPDATE findings SET status = ? WHERE id = ?", (status, finding_id)
    )
    await db.commit()


