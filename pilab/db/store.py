"""Typed helper functions for every INSERT / SELECT the agents and API need.

All functions accept an ``aiosqlite.Connection`` as their first argument.
Timestamps are UTC ISO 8601 strings.  IDs are ULIDs generated by the caller.
"""

from __future__ import annotations

import logging
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

import aiosqlite

from pilab import config
from pilab.db.migrate import apply_migrations

log = logging.getLogger(__name__)

Row = dict[str, Any]

SCHEMA_SQL = Path(__file__).parent / "schema.sql"


def _now() -> str:
    return datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")


def _row_factory(cursor: aiosqlite.Cursor, row: tuple) -> Row:
    cols = [d[0] for d in cursor.description]
    return dict(zip(cols, row))


# -------------------------------------------------------------------
# Database lifecycle
# -------------------------------------------------------------------

async def open_db(path: str | None = None) -> aiosqlite.Connection:
    """Open (and optionally initialise) the database."""
    path = path or config.DB_PATH
    db = await aiosqlite.connect(path)
    db.row_factory = _row_factory  # type: ignore[assignment]
    await db.execute(f"PRAGMA busy_timeout = {config.DB_BUSY_TIMEOUT_MS}")
    await db.execute("PRAGMA journal_mode = WAL")
    await db.execute("PRAGMA foreign_keys = ON")

    # Bootstrap schema if tables don't exist
    try:
        await db.execute("SELECT 1 FROM findings LIMIT 1")
    except aiosqlite.OperationalError:
        log.info("Initialising database schema from %s", SCHEMA_SQL)
        await db.executescript(SCHEMA_SQL.read_text())
        await db.commit()

    await apply_migrations(db)
    return db


