async def reap_stale_jobs(
    db: aiosqlite.Connection, timeout_seconds: int
) -> int:
    """Reset jobs claimed longer than *timeout_seconds* back to pending.
    Returns the number of reaped jobs."""
    cutoff = datetime.now(timezone.utc).timestamp() - timeout_seconds
    # SQLite stores ISO strings; compare lexicographically (works for ISO 8601)
    cutoff_str = datetime.fromtimestamp(cutoff, tz=timezone.utc).strftime(
        "%Y-%m-%dT%H:%M:%SZ"
    )
    cursor = await db.execute(
        "UPDATE job_queue SET status = 'pending', claimed_at = NULL, claimed_by = NULL "
        "WHERE status = 'claimed' AND claimed_at < ?",
        (cutoff_str,),
    )
    await db.commit()
    return cursor.rowcount


# -------------------------------------------------------------------
# Stats (for dashboard)
# -------------------------------------------------------------------

async def get_stats(db: aiosqlite.Connection) -> Row:
    stats: Row = {}

    async with db.execute(
        "SELECT status, COUNT(*) as cnt FROM findings GROUP BY status"
    ) as cur:
        for row in await cur.fetchall():
            stats[f"findings_{row['status']}"] = row["cnt"]

    async with db.execute(
        "SELECT status, COUNT(*) as cnt FROM projects GROUP BY status"
    ) as cur:
        for row in await cur.fetchall():
            stats[f"projects_{row['status']}"] = row["cnt"]

    async with db.execute("SELECT COUNT(*) as cnt FROM rejections") as cur:
        row = await cur.fetchone()
        stats["rejections_total"] = row["cnt"] if row else 0

    # Scouted this week (last 7 days)
    week_ago = datetime.now(timezone.utc)
    week_ago_str = week_ago.strftime("%Y-%m-%dT%H:%M:%SZ")
    # Approximate: subtract 7 days worth of seconds
    from datetime import timedelta
    week_ago = datetime.now(timezone.utc) - timedelta(days=7)
    week_ago_str = week_ago.strftime("%Y-%m-%dT%H:%M:%SZ")
    async with db.execute(
        "SELECT COUNT(*) as cnt FROM findings WHERE discovered_at >= ?",
        (week_ago_str,),
    ) as cur:
        row = await cur.fetchone()
        stats["scouted_this_week"] = row["cnt"] if row else 0

    # Last heartbeats per agent
    for agent in ("scout", "evaluator", "planner"):
        stats[f"{agent}_last_heartbeat"] = await last_heartbeat(db, agent)

    return stats


# -------------------------------------------------------------------
# Recent titles (for evaluator context)
# -------------------------------------------------------------------

async def recent_titles(
    db: aiosqlite.Connection, limit: int = 20
) -> list[str]:
    """Return titles of the most recent evaluated findings for novelty context."""
    async with db.execute(
        "SELECT f.title FROM findings f "
        "JOIN evaluations e ON e.finding_id = f.id "
        "ORDER BY e.evaluated_at DESC LIMIT ?",
        (limit,),
    ) as cur:
        rows = await cur.fetchall()
        return [r["title"] for r in rows]
